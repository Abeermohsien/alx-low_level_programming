#include "3-calc.h"
/**
 * op_add - adds two integer
 * @a: int
 * @b: int
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - adds two integer
 * @a: int
 * @b: int
 *
 * Return: the sum
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - adds two integer
 * @a: int
 * @b: int
 *
 * Return: the sum
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - adds two integer
 * @a: int
 * @b: int
 *
 * Return: the sum
 */
int op_div(int a, int b)
{
return (a % b);
}
